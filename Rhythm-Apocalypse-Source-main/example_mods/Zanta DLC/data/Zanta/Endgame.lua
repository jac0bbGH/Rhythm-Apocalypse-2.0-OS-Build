function onBeatHit()
	if curBeat >= 642 then
		setProperty('Fade.visible',true)
		setProperty('End.visible',true)
		setProperty('timeBarBG.visible',false)
		setProperty('timeBar.visible',false)
		setProperty('timeTxt.visible',false)
		setProperty('scoreTxt.visible',false)
		setProperty('healthBarBG.visible', false)
		setProperty('healthBar.visible', false)
		setProperty('iconP1.visible', false)
		setProperty('iconP2.visible', false)
		setProperty('bartop.visible', false)
		setProperty('barbot.visible', false)
		noteTweenX("aaaaaa5", 4, 9999, 0.001, "linear")
		noteTweenX("aaaaaa6", 5, 9999, 0.001, "linear")
		noteTweenX("aaaaaa7", 6, 9999, 0.001, "linear")
		noteTweenX("aaaaaa8", 7, 9999, 0.001, "linear")	
	end
end